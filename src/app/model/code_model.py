from timm.models.layers import DropPath, trunc_normal_

class Mlp(nn.Module):
    def __init__(self, in_features, hidden_features=None, out_features=None,
                 act_layer=nn.GELU, drop=0.):
        super().__init__()
        hidden_features = hidden_features or in_features
        out_features    = out_features    or in_features
        self.fc1   = nn.Linear(in_features, hidden_features)
        self.act   = act_layer()
        self.fc2   = nn.Linear(hidden_features, out_features)
        self.drop  = nn.Dropout(drop)

    def forward(self, x):
        return self.drop(self.fc2(self.drop(self.act(self.fc1(x)))))


class Attention(nn.Module):
    def __init__(self, dim, num_heads=8, qkv_bias=False, attn_drop=0., proj_drop=0.):
        super().__init__()
        self.num_heads = num_heads
        self.scale     = (dim // num_heads) ** -0.5
        self.qkv       = nn.Linear(dim, dim * 3, bias=qkv_bias)
        self.attn_drop = nn.Dropout(attn_drop)
        self.proj      = nn.Linear(dim, dim)
        self.proj_drop = nn.Dropout(proj_drop)

    def forward(self, x):
        B, N, C = x.shape
        qkv = self.qkv(x).reshape(B, N, 3, self.num_heads, C // self.num_heads).permute(2, 0, 3, 1, 4)
        q, k, v = qkv.unbind(0)
        attn = (q @ k.transpose(-2, -1)) * self.scale
        attn = self.attn_drop(attn.softmax(dim=-1))
        x = (attn @ v).transpose(1, 2).reshape(B, N, C)
        return self.proj_drop(self.proj(x))


class Block(nn.Module):
    def __init__(self, dim, num_heads, mlp_ratio=4., qkv_bias=False,
                 drop=0., attn_drop=0., drop_path=0., act_layer=nn.GELU):
        super().__init__()
        self.norm1     = nn.LayerNorm(dim)
        self.attn      = Attention(dim, num_heads=num_heads, qkv_bias=qkv_bias,
                                   attn_drop=attn_drop, proj_drop=drop)
        self.drop_path = DropPath(drop_path) if drop_path > 0. else nn.Identity()
        self.norm2     = nn.LayerNorm(dim)
        self.mlp       = Mlp(in_features=dim, hidden_features=int(dim * mlp_ratio),
                             act_layer=act_layer, drop=drop)

    def forward(self, x):
        x = x + self.drop_path(self.attn(self.norm1(x)))
        x = x + self.drop_path(self.mlp(self.norm2(x)))
        return x


# ── PointBERT Encoder ─────────────────────────────────────────

class PointBERTVoxelEncoder(nn.Module):
    """
    Full PointBERT voxel encoder (inline, tidak perlu import mc-retrieval).

    Pipeline:
      (B, 32, 32, 32) LongTensor
      → VoxelToPoints  → (B, M, 3) coords + (B, M) block_ids
      → Embedding(vocab, block_embed_dim) → (B, M, block_embed_dim)
      → Concat [xyz | block_feats] → (B, M, 3+block_embed_dim)
      → input_proj Linear → (B, M, trans_dim)
      → 12-layer Transformer → (B, M, trans_dim)
      → Global avg pool → (B, trans_dim)
      → output_head → (B, embed_dim)
    """

    def __init__(self, cfg: dict, vocab_size: int):
        super().__init__()
        trans_dim       = cfg["pb_trans_dim"]
        block_embed_dim = cfg["pb_block_embed_dim"]

        self.voxel_to_pts = VoxelToPoints(
            num_points=cfg["pb_num_points"],
            use_fps_eval=cfg["pb_use_fps_eval"]
        )
        self.block_embedding = nn.Embedding(vocab_size, block_embed_dim)
        self.input_proj      = nn.Linear(3 + block_embed_dim, trans_dim)

        # Transformer blocks
        dpr = [x.item() for x in torch.linspace(0, cfg["pb_drop_path"], cfg["pb_depth"])]
        self.blocks = nn.ModuleList([
            Block(
                dim=trans_dim,
                num_heads=cfg["pb_num_heads"],
                mlp_ratio=cfg["pb_mlp_ratio"],
                qkv_bias=True,
                drop=cfg["pb_dropout"],
                attn_drop=0.,
                drop_path=dpr[i],
            ) for i in range(cfg["pb_depth"])
        ])
        self.norm = nn.LayerNorm(trans_dim)

        # CLS token (opsional, kita pakai avg pool saja)
        self.output_head = nn.Sequential(
            nn.Linear(trans_dim, cfg["embed_dim"]),
            nn.Dropout(cfg["pb_dropout"]),
        )

        self._init_weights()
        self._load_pretrained(cfg["pb_pretrained_path"])

        if cfg["pb_freeze_backbone"]:
            for p in self.blocks.parameters():
                p.requires_grad = False
            for p in self.norm.parameters():
                p.requires_grad = False
            print("  PointBERT backbone: FROZEN")
        else:
            print("  PointBERT backbone: TRAINABLE (lr=5e-6)")

    def _init_weights(self):
        for m in self.modules():
            if isinstance(m, nn.Linear):
                trunc_normal_(m.weight, std=0.02)
                if m.bias is not None:
                    nn.init.zeros_(m.bias)
            elif isinstance(m, nn.LayerNorm):
                nn.init.ones_(m.weight)
                nn.init.zeros_(m.bias)

    def _load_pretrained(self, path: str):
        if not path or not os.path.exists(path):
            print(f"  ⚠️  Point-BERT weights not found at {path}. Training from scratch.")
            return
        ckpt = torch.load(path, map_location="cpu", weights_only=False)
        state = ckpt.get("model", ckpt)
        # Filter keys yang cocok
        model_state = self.state_dict()
        matched, missing, unexpected = {}, [], []
        for k, v in state.items():
            k_stripped = k.replace("module.", "").replace("base_model.", "")
            # Map transformer key names dari original PointBERT
            if k_stripped in model_state and model_state[k_stripped].shape == v.shape:
                matched[k_stripped] = v
            else:
                unexpected.append(k)
        self.load_state_dict(matched, strict=False)
        print(f"  PointBERT weights loaded: {len(matched)} matched, "
              f"{len(model_state) - len(matched)} randomly init")

    def forward(self, voxels: torch.LongTensor) -> torch.Tensor:
        xyz, bids = self.voxel_to_pts(voxels)    # (B, M, 3), (B, M)
        b_feat    = self.block_embedding(bids)    # (B, M, block_embed_dim)
        x = torch.cat([xyz, b_feat], dim=-1)     # (B, M, 3+block_embed_dim)
        x = self.input_proj(x)                   # (B, M, trans_dim)
        for blk in self.blocks:
            x = blk(x)
        x = self.norm(x)
        x = x.mean(dim=1)                        # (B, trans_dim) — global avg pool
        return self.output_head(x)

