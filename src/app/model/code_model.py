#%% 🔥 CELL 14 — Tri-Modal InfoNCE Loss
# ============================================================

class TriModalCLIPLoss(nn.Module):
    """
    Symmetric InfoNCE loss pada 3 pasang modalitas:
      L = λ_TI * L(T,I) + λ_TV * L(T,V) + λ_IV * L(I,V)

    Masing-masing menggunakan satu learnable temperature (shared).
    """

    def __init__(self, temperature_init: float = 0.07,
                 lambda_ti: float = 1.0,
                 lambda_tv: float = 1.0,
                 lambda_iv: float = 1.0):
        super().__init__()
        self.temperature = nn.Parameter(torch.tensor(temperature_init).log())
        self.lambda_ti   = lambda_ti
        self.lambda_tv   = lambda_tv
        self.lambda_iv   = lambda_iv

    def _infonce(self, a: torch.Tensor, b: torch.Tensor) -> torch.Tensor:
        """Symmetric InfoNCE loss between two sets of embeddings."""
        tau     = self.temperature.exp().clamp(0.01, 10.0)
        logits  = (a @ b.T) / tau                              # (B, B)
        labels  = torch.arange(a.shape[0], device=a.device)
        loss_ab = F.cross_entropy(logits,   labels)
        loss_ba = F.cross_entropy(logits.T, labels)
        return (loss_ab + loss_ba) / 2.0

    def forward(self, text_emb: torch.Tensor,
                image_emb: torch.Tensor,
                voxel_emb: torch.Tensor) -> Tuple[torch.Tensor, Dict]:
        l_ti = self._infonce(text_emb, image_emb)
        l_tv = self._infonce(text_emb, voxel_emb)
        l_iv = self._infonce(image_emb, voxel_emb)

        total = self.lambda_ti * l_ti + self.lambda_tv * l_tv + self.lambda_iv * l_iv

        return total, {
            "loss_TI": l_ti.item(),
            "loss_TV": l_tv.item(),
            "loss_IV": l_iv.item(),
            "temperature": self.temperature.exp().item(),
        }

