class TriModalDataset(Dataset):
    """
    Dataset yang mengembalikan (text, image_tensor, voxel_tensor, category) per item.

    Image loading:
      - train: random view dari 0–11
      - eval:  view_idx tetap (default view_06)
    """

    def __init__(self,
                 df: pd.DataFrame,
                 name2id: dict,
                 clip_processor: CLIPProcessor,
                 cfg: dict,
                 split: str = "train",
                 image_transform=None):
        self.df           = df.reset_index(drop=True)
        self.name2id      = name2id
        self.processor    = clip_processor
        self.cfg          = cfg
        self.split        = split
        self.is_train     = (split == "train")
        self.image_transform = image_transform

        # Pre-build text strings
        print(f"  [{split}] Building text strings ...")
        self.texts = [
            build_text(self.df.iloc[i], text_mode=cfg["text_mode"])
            for i in range(len(self.df))
        ]

        # Placeholder image (hitam) jika render tidak tersedia
        self.fallback_img = Image.new("RGB", (cfg["image_size"], cfg["image_size"]), (0, 0, 0))

    def __len__(self):
        return len(self.df)

    def __getitem__(self, idx):
        row = self.df.iloc[idx]

        # ── Teks ─────────────────────────────────────────────
        text = self.texts[idx]

        # ── Image ─────────────────────────────────────────────
        if self.is_train:
            view_idx = random.randint(0, self.cfg["n_views"] - 1)
        else:
            view_idx = self.cfg["eval_view_idx"]

        img_path = resolve_image_path(row, self.cfg["renders_base"],
                                      view_idx=view_idx,
                                      n_views=self.cfg["n_views"])
        try:
            img = Image.open(img_path).convert("RGB") if img_path else self.fallback_img
        except Exception:
            img = self.fallback_img

        # CLIP processor handles resize + normalize
        pixel_values = self.processor(images=img, return_tensors="pt")["pixel_values"].squeeze(0)

        # ── Voxel ─────────────────────────────────────────────
        vnd = row.get("voxel_name_data")
        voxel = remap_voxel_names(vnd, self.name2id,
                                  crop_bbox=self.cfg["crop_bbox"])

        if self.is_train and self.cfg["augment_voxel"]:
            if random.random() < self.cfg["voxel_aug_prob"]:
                voxel = augment_voxels(voxel.unsqueeze(0)).squeeze(0)
            if random.random() < self.cfg["voxel_aug_prob"]:
                voxel = voxel_block_dropout(voxel, p=self.cfg["voxel_dropout_prob"])

        # ── Category ──────────────────────────────────────────
        category = str(row.get("subtitle", "Unknown"))

        return text, pixel_values, voxel, category


def collate_fn(batch):
    texts, images, voxels, categories = zip(*batch)
    images = torch.stack(images)
    voxels = torch.stack(voxels)
    return list(texts), images, voxels, list(categories)

