@torch.no_grad()
def extract_all_embeddings(model: TriModalEncoder, loader: DataLoader, device):
    """Extract text, image, voxel embeddings + categories dari seluruh split."""
    model.eval()
    text_embs, image_embs, voxel_embs = [], [], []
    all_categories = []

    for texts, images, voxels, categories in tqdm(loader, desc="Encoding", leave=False):
        images = images.to(device)
        voxels = voxels.to(device)

        t = model.encode_text(texts)
        i = model.encode_image(images)
        v = model.encode_voxel(voxels)

        text_embs.append(t.cpu())
        image_embs.append(i.cpu())
        voxel_embs.append(v.cpu())
        all_categories.extend(categories)

    return (torch.cat(text_embs),
            torch.cat(image_embs),
            torch.cat(voxel_embs),
            all_categories)


def compute_instance_metrics(query_embs: torch.Tensor,
                             gallery_embs: torch.Tensor,
                             ks: List[int] = [1, 5, 10]) -> Dict:
    """Recall@K, MRR, Median Rank, Mean Rank (query[i] matches gallery[i])."""
    sim = query_embs @ gallery_embs.T   # (N, N)
    N   = sim.shape[0]
    ranks = []
    for i in range(N):
        scores  = sim[i].clone()
        scores[i] -= 1e9                 # exclude self
        sorted_idx = scores.argsort(descending=True)
        rank = (sorted_idx == i).nonzero(as_tuple=False)[0].item() + 1
        ranks.append(rank)
    ranks = np.array(ranks)

    metrics = {}
    for k in ks:
        metrics[f"recall@{k}"] = float((ranks <= k).mean())
    metrics["mrr"]         = float((1.0 / ranks).mean())
    metrics["median_rank"] = float(np.median(ranks))
    metrics["mean_rank"]   = float(np.mean(ranks))
    return metrics


def compute_category_metrics(query_embs: torch.Tensor,
                             gallery_embs: torch.Tensor,
                             categories: List[str],
                             ks: List[int] = [1, 5, 10]) -> Dict:
    """Category-level precision@K and hit_rate@K."""
    sim = query_embs @ gallery_embs.T
    N   = sim.shape[0]
    cat_arr = np.array(categories)
    metrics = {}

    for k in ks:
        precisions, hit_rates = [], []
        for i in range(N):
            scores = sim[i].clone()
            scores[i] -= 1e9
            top_k_idx = scores.argsort(descending=True)[:k]
            top_k_cats = cat_arr[top_k_idx.numpy()]
            correct = (top_k_cats == cat_arr[i])
            precisions.append(correct.mean())
            hit_rates.append(float(correct.any()))
        metrics[f"cat_precision@{k}"] = float(np.mean(precisions))
        metrics[f"cat_hit_rate@{k}"]  = float(np.mean(hit_rates))

    return metrics


def evaluate(model: TriModalEncoder, loader: DataLoader,
             device, cfg: dict, split_name: str = "test"):
    """Full evaluation: instance + category metrics untuk semua pair."""
    t_embs, i_embs, v_embs, cats = extract_all_embeddings(model, loader, device)
    N = t_embs.shape[0]
    print(f"\n{'='*60}")
    print(f"  EVALUATION — {split_name.upper()} ({N} items)")
    print(f"{'='*60}")

    ks = cfg["recall_k"]
    results = {}

    for (q_name, g_name, q_embs, g_embs) in [
        ("Text",  "Voxel", t_embs, v_embs),
        ("Voxel", "Text",  v_embs, t_embs),
        ("Text",  "Image", t_embs, i_embs),
        ("Image", "Voxel", i_embs, v_embs),
    ]:
        inst = compute_instance_metrics(q_embs, g_embs, ks)
        cat  = compute_category_metrics(q_embs, g_embs, cats, ks)

        tag = f"{q_name}→{g_name}"
        results[tag] = {**inst, **cat}

        print(f"\n  📌 {tag} — Instance-Level:")
        for k in ks:
            print(f"     recall@{k:2d}: {inst[f'recall@{k}']:.4f}")
        print(f"     mrr        : {inst['mrr']:.4f}")
        print(f"     median_rank: {inst['median_rank']:.1f}")
        print(f"     mean_rank  : {inst['mean_rank']:.1f}")

        print(f"\n  📊 {tag} — Category-Level:")
        for k in ks:
            print(f"     cat_precision@{k}: {cat[f'cat_precision@{k}']:.4f}  "
                  f"cat_hit_rate@{k}: {cat[f'cat_hit_rate@{k}']:.4f}")

    return results

