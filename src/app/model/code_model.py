class TinyClipTextEncoder(nn.Module):
    """
    TinyClip text encoder via HuggingFace CLIPModel.
    Output: (B, embed_dim) L2-normed embedding.
    """

    def __init__(self, cfg: dict, processor: CLIPProcessor):
        super().__init__()
        print(f"Loading TinyClip from {cfg['tinyclip_hf_model']} ...")
        clip = CLIPModel.from_pretrained(cfg["tinyclip_hf_model"])
        self.text_model  = clip.text_model
        self.text_proj   = clip.text_projection
        self.processor   = processor
        self.max_length  = min(cfg["text_max_length"], 77)

        # Freeze CLIP backbone
        if cfg["freeze_clip"]:
            for p in self.text_model.parameters():
                p.requires_grad = False
            for p in self.text_proj.parameters():
                p.requires_grad = False
            print("  TinyClip text: backbone FROZEN")

        clip_out_dim = clip.config.projection_dim  # e.g., 512 for TinyClip-39M
        self.proj = nn.Linear(clip_out_dim, cfg["embed_dim"])
        print(f"  TinyClip text proj: {clip_out_dim} → {cfg['embed_dim']}")

    def encode_text(self, texts: List[str]) -> torch.Tensor:
        device = next(self.parameters()).device
        inputs = self.processor(
            text=texts, padding=True, truncation=True,
            max_length=self.max_length, return_tensors="pt"
        ).to(device)
        out   = self.text_model(**inputs)
        # Use [CLS] pooled output
        feats = out.pooler_output                           # (B, hidden)
        feats = self.text_proj(feats)                      # (B, clip_out_dim)
        feats = self.proj(feats)                           # (B, embed_dim)
        return F.normalize(feats, dim=-1)

    def forward(self, texts: List[str]) -> torch.Tensor:
        return self.encode_text(texts)


class TinyClipImageEncoder(nn.Module):
    """
    TinyClip image encoder via HuggingFace CLIPModel.
    Output: (B, embed_dim) L2-normed embedding.
    """

    def __init__(self, cfg: dict):
        super().__init__()
        print(f"Loading TinyClip image encoder ...")
        clip = CLIPModel.from_pretrained(cfg["tinyclip_hf_model"])
        self.vision_model = clip.vision_model
        self.visual_proj  = clip.visual_projection

        if cfg["freeze_clip"]:
            for p in self.vision_model.parameters():
                p.requires_grad = False
            for p in self.visual_proj.parameters():
                p.requires_grad = False
            print("  TinyClip image: backbone FROZEN")

        clip_out_dim = clip.config.projection_dim
        self.proj = nn.Linear(clip_out_dim, cfg["embed_dim"])
        print(f"  TinyClip image proj: {clip_out_dim} → {cfg['embed_dim']}")

    def forward(self, pixel_values: torch.Tensor) -> torch.Tensor:
        out   = self.vision_model(pixel_values=pixel_values)
        feats = out.pooler_output                         # (B, hidden)
        feats = self.visual_proj(feats)                   # (B, clip_out_dim)
        feats = self.proj(feats)                          # (B, embed_dim)
        return F.normalize(feats, dim=-1)

