def train_one_epoch(model, criterion, loader, optimizer, scheduler,
                    device, scaler, use_amp=True):
    model.train()
    criterion.train()
    total_loss = 0.0
    loss_breakdown = {"loss_TI": 0., "loss_TV": 0., "loss_IV": 0.}
    n = 0

    pbar = tqdm(loader, desc="  train", leave=False)
    for texts, images, voxels, _categories in pbar:
        images = images.to(device)
        voxels = voxels.to(device)

        optimizer.zero_grad()

        with autocast("cuda", enabled=use_amp):
            t, i, v = model(texts, images, voxels)
            loss, info = criterion(t, i, v)

        scaler.scale(loss).backward()
        scaler.unscale_(optimizer)
        all_params = list(model.parameters()) + list(criterion.parameters())
        nn.utils.clip_grad_norm_(all_params, max_norm=1.0)
        scaler.step(optimizer)
        scaler.update()
        scheduler.step()

        total_loss += loss.item()
        for k in loss_breakdown:
            loss_breakdown[k] += info.get(k, 0.)
        n += 1
        pbar.set_postfix(
            loss=f"{loss.item():.4f}",
            τ=f"{info['temperature']:.3f}",
            TI=f"{info['loss_TI']:.3f}",
            TV=f"{info['loss_TV']:.3f}",
            IV=f"{info['loss_IV']:.3f}",
        )

    avg_loss = total_loss / max(n, 1)
    for k in loss_breakdown:
        loss_breakdown[k] /= max(n, 1)
    return avg_loss, loss_breakdown


@torch.no_grad()
def validate(model, criterion, loader, device, use_amp=True):
    model.eval()
    criterion.eval()
    total_loss = 0.0
    n = 0

    for texts, images, voxels, _categories in tqdm(loader, desc="  val  ", leave=False):
        images = images.to(device)
        voxels = voxels.to(device)
        with autocast("cuda", enabled=use_amp):
            t, i, v = model(texts, images, voxels)
            loss, _ = criterion(t, i, v)
        total_loss += loss.item()
        n += 1

    return total_loss / max(n, 1)


