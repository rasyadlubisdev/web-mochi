def load_dataset_and_build_loaders(cfg: dict):
    print("Loading parquet ...")
    df = pd.read_parquet(cfg["parquet_path"])
    print(f"  Total rows: {len(df)}")

    # Drop baris dengan voxel_name_data kosong
    df = df[df["voxel_name_data"].notna()].reset_index(drop=True)
    print(f"  After filtering: {len(df)}")

    # Build name vocab (Strategy 1)
    print("Building name vocabulary ...")
    name2id, id2name = build_name_vocab(df, max_types=cfg["max_block_types"])
    print(f"  Vocab size: {len(name2id)} block types")

    # Load TinyClip processor
    print(f"Loading TinyClip processor from {cfg['tinyclip_hf_model']} ...")
    processor = CLIPProcessor.from_pretrained(cfg["tinyclip_hf_model"])

    # Train / Val / Test split (stratified by subtitle)
    labels = df["subtitle"].fillna("Unknown").tolist()
    idx_all = list(range(len(df)))

    idx_train, idx_temp, _, labels_temp = train_test_split(
        idx_all, labels,
        test_size=cfg["val_split"] + cfg["test_split"],
        stratify=labels, random_state=cfg["seed"]
    )
    val_frac = cfg["val_split"] / (cfg["val_split"] + cfg["test_split"])
    idx_val, idx_test = train_test_split(
        idx_temp, test_size=1 - val_frac,
        stratify=labels_temp, random_state=cfg["seed"]
    )

    print(f"  Train: {len(idx_train)} | Val: {len(idx_val)} | Test: {len(idx_test)}")

    def make_loader(indices, split):
        ds = TriModalDataset(df.iloc[indices], name2id, processor, cfg, split=split)
        return DataLoader(
            ds, batch_size=cfg["batch_size"],
            shuffle=(split == "train"),
            num_workers=cfg["num_workers"],
            collate_fn=collate_fn,
            pin_memory=torch.cuda.is_available(),
            drop_last=(split == "train"),
        )

    train_loader = make_loader(idx_train, "train")
    val_loader   = make_loader(idx_val,   "val")
    test_loader  = make_loader(idx_test,  "test")

    return train_loader, val_loader, test_loader, name2id, id2name, processor

