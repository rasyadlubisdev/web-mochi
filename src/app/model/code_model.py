def build_name_vocab(df: pd.DataFrame, max_types: int = 672):
    """Strategy 1: build block vocabulary dari nama string."""
    counter: Counter = Counter()
    for vnd in df["voxel_name_data"]:
        try:
            names = list(vnd) if not isinstance(vnd, list) else vnd
            counter.update(n for n in names if isinstance(n, str) and n != "minecraft:air")
        except Exception:
            pass
    # index 0=air, 1=<rare>, 2..N=frequent blocks
    top_blocks = [b for b, _ in counter.most_common(max_types - 2)]
    name2id = {"minecraft:air": 0, "<rare>": 1}
    for i, name in enumerate(top_blocks, start=2):
        name2id[name] = i
    id2name = {v: k for k, v in name2id.items()}
    return name2id, id2name


def remap_voxel_names(voxel_name_flat, name2id: dict,
                      crop_bbox: bool = True, target_size: int = 32) -> torch.LongTensor:
    """Convert nama block list → compact ID → 32³ tensor."""
    names = list(voxel_name_flat)
    arr = np.array([name2id.get(n, 1) for n in names], dtype=np.int64)
    vol = arr.reshape(32, 32, 32)

    if crop_bbox:
        non_air = vol != 0
        if non_air.any():
            coords = np.argwhere(non_air)
            mins = coords.min(axis=0)
            maxs = coords.max(axis=0) + 1
            cropped = vol[mins[0]:maxs[0], mins[1]:maxs[1], mins[2]:maxs[2]]
            shape = cropped.shape
            if all(s > 0 for s in shape):
                factors = (target_size / shape[0],
                           target_size / shape[1],
                           target_size / shape[2])
                vol = zoom(cropped.astype(np.float32), factors, order=0).astype(np.int64)
                vol = np.clip(vol, 0, None)

    return torch.from_numpy(vol.reshape(32, 32, 32)).long()


def augment_voxels(voxels: torch.LongTensor) -> torch.LongTensor:
    """Random 90° Y-axis rotations + horizontal flips (sama dengan P6)."""
    k = torch.randint(0, 4, (1,)).item()
    if k > 0:
        voxels = torch.rot90(voxels, k, dims=(1, 3))
    if torch.rand(1).item() > 0.5:
        voxels = voxels.flip(dims=(1,))
    if torch.rand(1).item() > 0.5:
        voxels = voxels.flip(dims=(3,))
    return voxels


def voxel_block_dropout(voxels: torch.LongTensor, p: float = 0.05) -> torch.LongTensor:
    """Randomly drop non-air blocks to 0 (augmentasi voxel)."""
    mask = (voxels != 0) & (torch.rand_like(voxels.float()) < p)
    return voxels.masked_fill(mask, 0)

