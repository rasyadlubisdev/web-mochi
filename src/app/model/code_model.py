def main():
    import random as pyrandom
    pyrandom.seed(CFG["seed"])
    np.random.seed(CFG["seed"])
    torch.manual_seed(CFG["seed"])
    if torch.cuda.is_available():
        torch.cuda.manual_seed_all(CFG["seed"])

    # ── Load Data ──────────────────────────────────────────────
    print("\n" + "="*60)
    print("  📂 Loading Dataset")
    print("="*60)
    train_loader, val_loader, test_loader, name2id, id2name, processor = \
        load_dataset_and_build_loaders(CFG)

    vocab_size = len(name2id)
    print(f"  Vocab size: {vocab_size}")
    print(f"  Train batches: {len(train_loader)}")

    # ── Build Model ────────────────────────────────────────────
    print("\n" + "="*60)
    print("  🧠 Building TriModal Model")
    print("="*60)
    model     = TriModalEncoder(CFG, vocab_size, processor).to(DEVICE)
    criterion = TriModalCLIPLoss(
        temperature_init=CFG["temperature_init"],
        lambda_ti=CFG["lambda_ti"],
        lambda_tv=CFG["lambda_tv"],
        lambda_iv=CFG["lambda_iv"],
    ).to(DEVICE)

    # Param count
    trainable = sum(p.numel() for p in model.parameters() if p.requires_grad)
    total     = sum(p.numel() for p in model.parameters())
    print(f"\n  Trainable: {trainable:,} / {total:,} params")

    # ── Semantic Init (Strategy 2) ─────────────────────────────
    if CFG["use_semantic_init"]:
        print("\n  Applying semantic block embedding init (Strategy 2) ...")
        with torch.no_grad():
            block_names = [id2name.get(i, "unknown block") for i in range(vocab_size)]
            # Encode block names dengan text encoder
            batch_size = 64
            all_embs = []
            for start in range(0, len(block_names), batch_size):
                chunk = block_names[start:start+batch_size]
                emb = model.encode_text(chunk).cpu()
                all_embs.append(emb)
            all_embs = torch.cat(all_embs, dim=0)           # (vocab, 256)

            # PCA down ke block_embed_dim (128)
            block_embed_dim = CFG["pb_block_embed_dim"]
            U, S, V = torch.pca_lowrank(all_embs, q=block_embed_dim)
            reduced = U * S
            reduced = reduced / (reduced.std() + 1e-8) * 0.1
            model.voxel_encoder.block_embedding.weight.data.copy_(
                reduced.to(DEVICE)
            )
        print("  ✅ Semantic init applied")

    # ── Optimizer & Scheduler ──────────────────────────────────
    steps_per_epoch    = len(train_loader)
    num_training_steps = CFG["epochs"] * steps_per_epoch

    optimizer, scheduler = build_optimizer_scheduler(
        model, criterion, CFG, num_training_steps
    )
    scaler = GradScaler(enabled=CFG["use_amp"])

    # ── Training Loop ──────────────────────────────────────────
    print("\n" + "="*60)
    print("  🏋️  Training")
    print("="*60)

    best_val_loss    = float("inf")
    patience_counter = 0
    history          = []

    for epoch in range(1, CFG["epochs"] + 1):
        t0 = time.time()

        train_loss, breakdown = train_one_epoch(
            model, criterion, train_loader, optimizer, scheduler,
            DEVICE, scaler, use_amp=CFG["use_amp"]
        )
        val_loss = validate(model, criterion, val_loader, DEVICE,
                            use_amp=CFG["use_amp"])

        elapsed = time.time() - t0
        print(f"  Epoch {epoch:03d}/{CFG['epochs']} | "
              f"train={train_loss:.4f} (TI={breakdown['loss_TI']:.3f} "
              f"TV={breakdown['loss_TV']:.3f} IV={breakdown['loss_IV']:.3f}) | "
              f"val={val_loss:.4f} | {elapsed:.1f}s")

        history.append({"epoch": epoch, "train": train_loss, "val": val_loss})

        # Checkpoint best model
        if val_loss < best_val_loss:
            best_val_loss    = val_loss
            patience_counter = 0
            save_checkpoint(
                os.path.join(CFG["checkpoint_dir"], "best_model.pth"),
                model, criterion, optimizer, epoch, best_val_loss
            )
            print(f"  ✅ Best model saved (val={best_val_loss:.4f})")
        else:
            patience_counter += 1
            if patience_counter >= CFG["early_stopping_patience"]:
                print(f"\n  ⏹️  Early stopping at epoch {epoch}")
                break

        # Periodic checkpoint
        if epoch % 10 == 0:
            save_checkpoint(
                os.path.join(CFG["checkpoint_dir"], f"epoch_{epoch:03d}.pth"),
                model, criterion, optimizer, epoch, best_val_loss
            )

    # ── Final Evaluation on Test Set ──────────────────────────
    print("\n" + "="*60)
    print("  📊 Final Evaluation on TEST SET")
    print("="*60)

    # Load best checkpoint
    best_ckpt = os.path.join(CFG["checkpoint_dir"], "best_model.pth")
    if os.path.exists(best_ckpt):
        ckpt = torch.load(best_ckpt, map_location=DEVICE, weights_only=False)
        model.load_state_dict(ckpt["model_state"])
        print(f"  Loaded best model from epoch {ckpt['epoch']}")

    test_results = evaluate(model, test_loader, DEVICE, CFG, split_name="test")

    # ── Save to Drive ──────────────────────────────────────────
