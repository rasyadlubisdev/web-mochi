def resolve_image_path(row: pd.Series, renders_base: str,
                       view_idx: int = None, n_views: int = 12) -> Optional[str]:
    """
    Resolve path gambar dari row parquet.
    Handles kasus path di parquet berbeda dengan lokasi aktual.
    """
    if view_idx is None:
        view_idx = random.randint(0, n_views - 1)

    # Coba kolom view_XX_path langsung
    col = f"view_{view_idx:02d}_path"
    stored_path = row.get(col)

    if isinstance(stored_path, str) and stored_path:
        # Coba path langsung
        if os.path.exists(stored_path):
            return stored_path
        # Ambil basename folder + filename dan join dengan renders_base
        parts = Path(stored_path).parts
        if len(parts) >= 2:
            folder_name = parts[-2]
            file_name   = parts[-1]
            candidate = os.path.join(renders_base, folder_name, file_name)
            if os.path.exists(candidate):
                return candidate

    # Fallback: scan renders_base dengan render_folder kolom
    render_folder = row.get("render_folder")
    if isinstance(render_folder, str):
        candidate = os.path.join(renders_base, render_folder,
                                 f"view_{view_idx:02d}.jpg")
        if os.path.exists(candidate):
            return candidate

    return None  # image tidak ditemukan

