def build_optimizer_scheduler(model: TriModalEncoder,
                               criterion: TriModalCLIPLoss,
                               cfg: dict, num_training_steps: int):
    """Param groups dengan differential LR sesuai optimal P6."""

    # TinyClip text projection (trainable)
    text_proj_params = list(model.text_encoder.proj.parameters())

    # TinyClip image projection (trainable)
    img_proj_params = list(model.image_encoder.proj.parameters())

    # PointBERT adapter + head (selalu trainable)
    pb_adapter_params = (
        list(model.voxel_encoder.block_embedding.parameters()) +
        list(model.voxel_encoder.input_proj.parameters())
    )
    pb_head_params = list(model.voxel_encoder.output_head.parameters())

    # PointBERT backbone (unfreeze dengan lr sangat kecil)
    pb_backbone_params = (
        list(model.voxel_encoder.blocks.parameters()) +
        list(model.voxel_encoder.norm.parameters())
    )

    # Temperature
    temp_params = list(criterion.parameters())

    param_groups = [
        {"name": "text_proj",    "params": text_proj_params,    "lr": cfg["lr_text_proj"]},
        {"name": "image_proj",   "params": img_proj_params,     "lr": cfg["lr_image_proj"]},
        {"name": "pb_adapter",   "params": pb_adapter_params,   "lr": cfg["lr_pb_adapter"]},
        {"name": "pb_head",      "params": pb_head_params,      "lr": cfg["lr_pb_head"]},
        {"name": "pb_backbone",  "params": pb_backbone_params,  "lr": cfg["lr_pb_backbone"]},
        {"name": "temperature",  "params": temp_params,         "lr": cfg["lr_pb_adapter"]},
    ]

    optimizer = AdamW(param_groups, weight_decay=cfg["weight_decay"])

    # Warmup + Cosine annealing
    warmup_steps = int(num_training_steps * cfg["warmup_ratio"])
    warmup_sched  = LinearLR(optimizer, start_factor=0.1, end_factor=1.0,
                             total_iters=warmup_steps)
    cosine_sched  = CosineAnnealingLR(optimizer,
                                      T_max=num_training_steps - warmup_steps,
                                      eta_min=1e-7)
    scheduler = SequentialLR(optimizer, schedulers=[warmup_sched, cosine_sched],
                             milestones=[warmup_steps])

    # Print param groups
    print("\nOptimizer param groups:")
    for g in param_groups:
        n = sum(p.numel() for p in g["params"])
        print(f"  [{g['name']:15s}] lr={g['lr']:.1e}  params={n:,}")

    return optimizer, scheduler

