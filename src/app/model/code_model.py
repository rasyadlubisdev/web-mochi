    shutil.copy(best_ckpt, os.path.join(drive_save_dir, "best_model.pth"))

    # Save results JSON
    import json
    with open(os.path.join(drive_save_dir, "test_results.json"), "w") as f:
        json.dump(test_results, f, indent=2)

    print(f"\n  ✅ Results saved to {drive_save_dir}")

    # ── Print Summary ──────────────────────────────────────────
    print("\n" + "="*60)
    print("  📋 RESULT SUMMARY")
    print("="*60)
    tv = test_results.get("Text→Voxel", {})
    print("  Text → Voxel (MAIN TASK):")
    for k in CFG["recall_k"]:
        print(f"    recall@{k:2d}    = {tv.get(f'recall@{k}', 0):.4f}")
    print(f"    mrr         = {tv.get('mrr', 0):.4f}")
    print(f"    median_rank = {tv.get('median_rank', 0):.1f}")
    print(f"    mean_rank   = {tv.get('mean_rank', 0):.1f}")

    for k in CFG["recall_k"]:
        print(f"    cat_hit@{k:2d}  = {tv.get(f'cat_hit_rate@{k}', 0):.4f}")

    # Bimodal baseline comparison
    print("\n  ─── vs. Bimodal Baseline (Percobaan 6) ───")
    bimodal = {"recall@1": 0.0768, "recall@5": 0.2161,
               "recall@10": 0.3337, "mrr": 0.1589, "median_rank": 22.0}
    for m, bv in bimodal.items():
        tv_v = tv.get(m, 0)
        delta = tv_v - bv
        sign  = "+" if delta >= 0 else ""
        print(f"    {m:12s}: {tv_v:.4f}  (Δ {sign}{delta:.4f} vs bimodal {bv:.4f})")

